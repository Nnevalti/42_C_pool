char	*ft_strlowcase(char *str)
{
	char *pointer;

	pointer = str;
	while (*str != '\0')
	{
		if (*str >= 65 && *str <= 90)
		{
			*str += 32;
		}
		str++;
	}
	return (pointer);
}
